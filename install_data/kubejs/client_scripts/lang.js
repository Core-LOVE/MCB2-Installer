const blocksetChecks = [
    "",
    "_stairs",
    "_slab",
    "_wall"
]

let blocksetReplacement = {
    "": "",
    "bricks": "brick",
    "tiles": "tile",
}
const blocksetRename = (name, callback) => {
    blocksetChecks.forEach(variant => {
        let block = (name + variant)

        for (const key in blocksetReplacement) {
            block = block.replace(key, blocksetReplacement[key])

            if (Item.exists(block)) {
                let lang = callback(block)
                lang = lang.replace(/\w+:/, "")
                lang = lang.replace(/_/g, " ")
                lang = lang.replace(/\b\w/g, (match) => match.toUpperCase())

                // console.log(block, lang)

                renames[block] = lang
            }
        }
    })
}

